@WebServlet(value = ["/sqli-00/BenchmarkTest00191"])
class BenchmarkTest00191 : HttpServlet() {
    @Throws(ServletException::class, IOException::class)
    public override fun doGet(request: HttpServletRequest, response: HttpServletResponse) {
        doPost(request, response)
    }

    @Throws(ServletException::class, IOException::class)
    public override fun doPost(request: HttpServletRequest, response: HttpServletResponse) {
        response.contentType = "text/html;charset=UTF-8"
        var param = ""
        if (request.getHeader("BenchmarkTest00191") != null) {
            param = request.getHeader("BenchmarkTest00191")
        }

        // URL Decode the header value since req.getHeader() doesn't. Unlike req.getParameter().
        param = URLDecoder.decode(param, "UTF-8")
        val bar: String
        val guess = "ABC"
        val switchTarget = guess[1] // condition 'B', which is safe
        bar = when (switchTarget) {
            'A' -> param
            'B' -> "bob"
            'C', 'D' -> param
            else -> "bob's your uncle"
        }
        val sql = "{call $bar}"
        try {
            val connection = DatabaseHelper.getSqlConnection()
            val statement = connection.prepareCall(
                sql, ResultSet.TYPE_FORWARD_ONLY,
                ResultSet.CONCUR_READ_ONLY
            )
            val rs = statement.executeQuery()
            DatabaseHelper.printResults(rs, sql, response)
        } catch (e: SQLException) {
            if (DatabaseHelper.hideSQLErrors) {
                response.writer.println(
                    "Error processing request."
                )
                return
            } else throw ServletException(e)
        }
    }

    companion object {
        private const val serialVersionUID = 1L
    }
}